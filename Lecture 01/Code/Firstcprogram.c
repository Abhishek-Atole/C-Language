#include<stdio.h> // This is a preprocessor directive that includes the standard input/output header file. 
                  // It allows the use of functions like printf().

int main()        // This is the main function. It's the entry point of the program where execution begins.
{ 
    printf("Hello World!\n"); // The printf() function is used to output the text "Hello World" followed by a newline character (\n).
    
    return 0;      // The return statement ends the main function and returns the value 0 to the operating system, 
                   // which typically signifies that the program executed successfully.
}
